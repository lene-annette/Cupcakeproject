package entities;

/**
 *
 * @author lene_
 */
public class Bottom {
    private String type;
    private int price;

    public Bottom(String type, int price) {
        this.type = type;
        this.price = price;
    }

    public Bottom() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    
    
}
